#import "E20ContentViewController.h"
#import "E20ContentItem.h"

@interface E20ContentViewController ()

@property NSMutableArray *contentItems;

@end

@implementation E20ContentViewController

- (void)loadInitialData {
    E20ContentItem *item1 = [[E20ContentItem alloc] init];
    item1.itemName = @"Record/view sensor data";
    [self.contentItems addObject:item1];
    E20ContentItem *item2 = [[E20ContentItem alloc] init];
    item2.itemName = @"Eaton Center Map";
    [self.contentItems addObject:item2];
    E20ContentItem *item3 = [[E20ContentItem alloc] init];
    item3.itemName = @"Orientation Scheme";
    [self.contentItems addObject:item3];

}


- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    self.contentItems = [[NSMutableArray alloc] init];
    [self loadInitialData];

}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{

    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{

    // Return the number of rows in the section.
    return [self.contentItems count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"ListPrototypeCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    E20ContentItem *contentItem = [self.contentItems objectAtIndex:indexPath.row];
    cell.textLabel.text = contentItem.itemName;


    
    return cell;
}

